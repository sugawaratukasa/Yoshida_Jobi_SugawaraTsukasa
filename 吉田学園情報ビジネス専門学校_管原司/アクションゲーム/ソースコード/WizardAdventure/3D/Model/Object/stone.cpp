//******************************************************************************
// マクロ定義
//******************************************************************************
#define ROT			(D3DXVECTOR3(0.0f,D3DXToRadian(fRot),0.0f))	// 向き
#define RANDOM_ROT	(360)										// ランダムの向き
//******************************************************************************
//静的メンバ変数
//******************************************************************************

//******************************************************************************
// コンストラクタ
//******************************************************************************
CStone::CStone(int nPriority) : C3D_Obj(nPriority)
{
}
//******************************************************************************
// デストラクタ
//******************************************************************************
CStone::~CStone()
{

}
//******************************************************************************
// 生成関数
//******************************************************************************
CStone * CStone::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, C3D_Obj::TYPE Obj)
{
	// CStoneのポインタ
	CStone *pStone = NULL;

	// NULLの場合
	if (pStone == NULL)
	{
		// メモリ確保
		pStone = new CStone;

		// NULLでない場合
		if (pStone != NULL)
		{
			// ランダム
			float fRot = float(rand() % RANDOM_ROT);

			// 情報設定
			pStone->SetModel(pos, ROT, size, Obj);

			// オブジェクトタイプ設定
			pStone->SetObjType(OBJTYPE_BACK_OBJ);

			// 初期化
			pStone->Init();
		}
	}
	// ポインタを返す
	return pStone;
}
//******************************************************************************
// 初期化処理
//******************************************************************************
HRESULT CStone::Init(void)
{
	// 初期化
	C3D_Obj::Init();

	return S_OK;
}
//******************************************************************************
// 終了処理
//******************************************************************************
void CStone::Uninit(void)
{
	// 終了
	C3D_Obj::Uninit();
}
//******************************************************************************
// 更新処理
//******************************************************************************
void CStone::Update(void)
{
	// 更新
	C3D_Obj::Update();
}

//******************************************************************************
// 描画処理
//******************************************************************************
void CStone::Draw(void)
{
	// 描画
	C3D_Obj::Draw();
}