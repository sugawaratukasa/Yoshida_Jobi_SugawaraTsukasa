#define ROT			(D3DXVECTOR3(0.0f,D3DXToRadian(90.0f),0.0f))	// 向き
//******************************************************************************
//静的メンバ変数
//******************************************************************************

//******************************************************************************
// コンストラクタ
//******************************************************************************
CWood::CWood(int nPriority) : C3D_Obj(nPriority)
{
}
//******************************************************************************
// デストラクタ
//******************************************************************************
CWood::~CWood()
{

}
//******************************************************************************
// 生成関数
//******************************************************************************
CWood * CWood::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	// CWoodのポインタ
	CWood *pWood = NULL;

	// NULLの場合
	if (pWood == NULL)
	{
		// メモリ確保
		pWood = new CWood;

		// NULLでない場合
		if (pWood != NULL)
		{
			// 情報設定
			pWood->SetModel(pos, ROT, size, C3D_Obj::TYPE_WOOD);

			// 初期化
			pWood->Init();
		}
	}
	// ポインタを返す
	return pWood;
}
//******************************************************************************
// 初期化処理
//******************************************************************************
HRESULT CWood::Init(void)
{
	// 初期化
	C3D_Obj::Init();

	return S_OK;
}
//******************************************************************************
// 終了処理
//******************************************************************************
void CWood::Uninit(void)
{
	// 終了
	C3D_Obj::Uninit();
}
//******************************************************************************
