//******************************************************************************
// コンストラクタ
//******************************************************************************
CParticle_Emitter::CParticle_Emitter()
{
	m_pos		= INIT_D3DXVECTOR3;
	m_nCount	= INIT_INT;
}
//******************************************************************************
// デストラクタ
//******************************************************************************
CParticle_Emitter::~CParticle_Emitter()
{
}
//******************************************************************************
// 更新
//******************************************************************************
void CParticle_Emitter::Update(void)
{
	// インクリメント
	m_nCount++;
}
//******************************************************************************
// 位置設定
//******************************************************************************
void CParticle_Emitter::SetPosition(D3DXVECTOR3 pos)
{
	m_pos = pos;
}